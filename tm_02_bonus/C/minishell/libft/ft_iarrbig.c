#include <libft.h>

int		ft_iarrbig(int *arr, int len)
{
	int i;
	int temp;

	i = 0;
	temp = -2147483647;
	while (i < len)
	{
		if (arr[i] > temp)
			temp = arr[i];
		i++;
	}
	return (temp);
}
